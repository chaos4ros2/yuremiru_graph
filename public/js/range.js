const range1 = document.querySelector('#range-1');
const range2 = document.querySelector('#range-2');

const startValue = document.querySelector('#start-value');
const endValue = document.querySelector('#end-value');
const difference = document.querySelector('#difference');

const play_btn = document.querySelector('#play_btn');
// 間隔セッター
var interval_time = 0;

/**
 * 差分を記録する
*/
function setDifference() {
    const val = Number(range2.value) - Number(range1.value);
    difference.textContent = val.toFixed(2);
}

// スライドスタートのイベント
range1.addEventListener('input', (evt) => {
    startValue.textContent = evt.target.value;
    setDifference();
    plot(evt.target.value, document.getElementById('range-2').value);
})

// スライドエンドのイベント
range2.addEventListener('input', (evt) => {
    endValue.textContent = evt.target.value;
    setDifference();
    plot(document.getElementById('range-1').value, evt.target.value);
})

// 画面開いた後最初の処理
startValue.textContent = range1.value;
endValue.textContent = range2.value;
setDifference();
plot(range1.value, range2.value);

// Playボタンのイベント
play_btn.addEventListener('click', () => {
    interval_time = document.getElementById('interval_time').value;
    console.log(interval_time);
    play();
    // playボタン無効化
    document.getElementById('play_btn').disabled = true;
});

function play(range1_value = 0, range2_value = 0) {
    // データの範囲を超えると終了
    if (range2_value >= range2.max) {
        document.getElementById('play_btn').disabled = false;
        return;
    }
    window.setTimeout(() => {
        console.log(interval_time);
        startValue.textContent = range1_value;
        endValue.textContent = range2_value;
        range1.value = range1_value;
        range2.value = range2_value;
        setDifference();
        plot(range1_value, range2_value);
        // 再帰呼び出し
        if (interval_time && range2_value - range1_value >= interval_time)
            play(range1_value + 0.1, range2_value + 0.1);
        else 
            play(range1_value, range2_value + 0.1);
    }, 100);
}

function plot(range_1, range_2) {
    const data = get_data();
    
    var pointCount = data.length;
    var i, r;
    
    var x = [];
    var y = [];
    var z = [];
    var c = [];
    
    for (i = 0; i < pointCount; i++) 
    {
        if (data[i][0] >= range_1 && data[i][0] <= range_2) { 
            // r = 10 * Math.cos(i / 10);
            x.push(data[i][1]);
            y.push(data[i][2]);
            z.push(data[i][3]);
            c.push(i);
        }
    }
    
    Plotly.newPlot('myDiv', [{
        type: 'scatter3d',
        mode: 'lines+markers',
        x: x,
        y: y,
        z: z,
        line: {
            width: 6,
            color: c,
            colorscale: "Viridis"
        },
        marker: {
            size: 0.5,
            color: c,
            colorscale: "Greens",
            cmin: -20,
            cmax: 50
        }},                  
    ]);
}

